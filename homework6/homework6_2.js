function findValueByKey(companyName, company) {
    function findCompany(companyObj, name) {
        if (companyObj.name === name) {
            return companyObj;
        } else if (companyObj.clients) {
            for (let client of companyObj.clients) {
                const result = findCompany(client, name);
                if (result) {
                    return result;
                }
            }
        }
    }

    return findCompany(company, companyName);
}

